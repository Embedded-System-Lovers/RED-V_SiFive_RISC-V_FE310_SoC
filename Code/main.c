//=====================================================================================================
// Includes
//=====================================================================================================
#include "FE310.h"
#include "Platform_Types.h"
#include <string.h>

//=====================================================================================================
// Functions prototype
//=====================================================================================================
void FE310_HwInitialization(void);
void _delay(unsigned long d);

//=====================================================================================================
// Macros
//=====================================================================================================
#define DELAY_CNT   8000000ul


//-----------------------------------------------------------------------------------------
/// \brief  
///
/// \param  
///
/// \return 
//-----------------------------------------------------------------------------------------
int main(void)
{
  for(;;)
  {
    GPIO0->output_val.bit.pin5 ^= 1;
    _delay(DELAY_CNT);
  }
  return(0);
}

//-----------------------------------------------------------------------------------------
/// \brief  
///
/// \param  
///
/// \return 
//-----------------------------------------------------------------------------------------
void FE310_HwInitialization(void)
{
  GPIO0->output_en.bit.pin5 = 1;
  GPIO0->output_val.bit.pin5 = 1;

}
